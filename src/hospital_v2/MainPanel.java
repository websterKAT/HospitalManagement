    private void cmdLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLoginActionPerformed
        String posi = (String) loginAs.getSelectedItem();
        if(loginAuthentication(txtUserName.getText(), txtPassword.getText(),posi)){
            if(posi.equals("ADMINISTRATOR")){
                cmdPersonal.setEnabled(true);
                cmdDoctors.setEnabled(true);
                cmdNurses.setEnabled(true);
                cmdLabtech.setEnabled(true);
                cmdEmployee.setEnabled(true);
                
                
                
            }
            else if(posi.equals("RECEPTIONIST")){
                cmdEmployee.setEnabled(true);
                cmdPersonal.setEnabled(true);
                cmdDoctors.setEnabled(false);
                cmdNurses.setEnabled(false);
                cmdLabtech.setEnabled(false);
                
                
                new Employee().show();
            }
            else if(posi.equals("DOCTOR")){
                cmdDoctors.setEnabled(true);
                cmdPersonal.setEnabled(false);
                cmdNurses.setEnabled(false);
                cmdLabtech.setEnabled(false);
                cmdEmployee.setEnabled(false);
                
                new DoctorLogin().show();
            }
            else if(posi.equals("NURSE")){
                cmdNurses.setEnabled(true);
                cmdPersonal.setEnabled(false);
                cmdDoctors.setEnabled(false);
                cmdLabtech.setEnabled(false);
                cmdEmployee.setEnabled(false);
                
                new NurseLogin().show();
            }
            else if(posi.equals("LAB TECHNICIAN")){
                cmdLabtech.setEnabled(true);
                cmdPersonal.setEnabled(false);
                cmdDoctors.setEnabled(false);
                cmdNurses.setEnabled(false);
                cmdEmployee.setEnabled(false);
                
                new LabTectLogin().show();
            }
        }
        else {
            JOptionPane.showMessageDialog(null,"Invalid userName or password");
        }
    }//GEN-LAST:event_cmdLoginActionPerformed


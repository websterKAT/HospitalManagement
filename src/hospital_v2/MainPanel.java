    public boolean loginAuthentication(String user, String password, String pos){
        boolean flag = false;
        try {
            String sqlQuary = "Select userName,password,pos from login where userName='"+user+"' and password ='"+password+"'";
            PreparedStatement ps = conn.prepareStatement(sqlQuary);
            ResultSet rs = ps.executeQuery();
            if(rs.next()){
                String USER = rs.getString("userName");
                String PASSWORD = rs.getString("password");
                String POS = rs.getString("pos");
                
                if(USER.equals(user) && PASSWORD.equals(password) && POS.equals(pos)){
                    flag = true;
                     JOptionPane.showMessageDialog(null, "Authentication Success");
                   
                }
                else{
                    flag = false;
                    JOptionPane.showMessageDialog(null, "Authentication Failed");
                }
               
                return flag;
            }
        }
        
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Problem:"+e);
            return flag;
            
        }
        return flag;
    }       
  
    
  

